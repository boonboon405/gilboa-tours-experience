import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import "https://deno.land/x/xhr@0.1.0/mod.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const OPENAI_API_KEY = Deno.env.get('OPENAI_API_KEY');
    if (!OPENAI_API_KEY) {
      console.error('OPENAI_API_KEY is not set');
      throw new Error('OPENAI_API_KEY is not configured');
    }

    const { language = 'he', voice = 'alloy' } = await req.json().catch(() => ({}));
    
    // Build system instructions based on language
    const systemInstructions = language === 'he' 
      ? `אתה סוכן סיורים מקצועי המתמחה בצפון ישראל - גליל, כנרת, הרי הגלבוע ועמק המעיינות.
         דבר בעברית רשמית וברורה. היה ידידותי, מקצועי ומסביר פנים.
         עזור למבקרים לתכנן סיורים, המלץ על אטרקציות ופעילויות.
         אל תשתמש בסלנג או במילים לא פורמליות.`
      : `You are a professional tour agent specializing in Northern Israel - Galilee, Sea of Galilee, Gilboa Mountains, and Springs Valley.
         Be friendly, professional, and helpful.
         Help visitors plan tours and recommend attractions and activities.`;

    // Validate voice - only use supported voices
    const supportedVoices = ['alloy', 'ash', 'ballad', 'coral', 'echo', 'sage', 'shimmer', 'verse'];
    const selectedVoice = supportedVoices.includes(voice) ? voice : 'alloy';

    console.log(`Creating realtime session - Language: ${language}, Voice: ${selectedVoice}`);

    // Request an ephemeral token from OpenAI
    const response = await fetch("https://api.openai.com/v1/realtime/sessions", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${OPENAI_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-4o-realtime-preview-2024-12-17",
        voice: selectedVoice,
        instructions: systemInstructions,
        input_audio_format: "pcm16",
        output_audio_format: "pcm16",
        input_audio_transcription: {
          model: "whisper-1"
        },
        turn_detection: {
          type: "server_vad",
          threshold: 0.5,
          prefix_padding_ms: 300,
          silence_duration_ms: 1000
        }
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("OpenAI API error:", response.status, errorText);
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    const data = await response.json();
    console.log("Session created successfully:", data.id);

    return new Response(JSON.stringify(data), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  } catch (error: unknown) {
    const errorMessage = error instanceof Error ? error.message : 'Unknown error';
    console.error("Error creating realtime session:", errorMessage);
    return new Response(JSON.stringify({ error: errorMessage }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
